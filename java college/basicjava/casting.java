package basicjava;
class casting{
    public static void main(String args[]){
        double mydouble = 4.45;
        int myint= (int) mydouble; /* narrow casting and widening casting */
        
        System.out.println(myint);
        System.out.println(mydouble);
        int myx =15;
        myx+=5;
System.out.println(myx);


    }
}