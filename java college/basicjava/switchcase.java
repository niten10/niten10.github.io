package basicjava;

public class switchcase {
    public static void main(String [] args){
        int day=6;
               switch(day){
                case 1:
                System.out.println("sun");
                break;
                case 2:
                  System.out.println("mon");
                  break;

                  case 3:
                     System.out.println("tue");
                     break;

                  default:
                      System.out.println(" no data found");
                      break;
                 


               }    
}
}